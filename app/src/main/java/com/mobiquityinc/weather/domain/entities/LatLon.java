package com.mobiquityinc.weather.domain.entities;

class LatLon {

    private double lon;
    private double lat;

    public LatLon() {
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

}
