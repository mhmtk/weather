package com.mobiquityinc.weather.entities;

class LonLat {

    private double lon;
    private double lat;

    public LonLat() {
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }
}
